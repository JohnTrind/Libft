#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*ret;
	const unsigned char	*s;
	int					i;

	if (!src && !dest)
		return (0);
	s = src;
	ret = dest;
	i = 0;
	while (n-- != 0)
	{
		ret[i] = s[i];
		i++;
	}
	return (dest);
}
